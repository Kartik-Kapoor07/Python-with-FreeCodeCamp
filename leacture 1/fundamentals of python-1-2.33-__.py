# Major types of Python modules:

# 1. Built-in modules:
# These come pre-installed with Python
# Example: math, random, os, time, sys

# 2. Standard library modules:
# Official Python modules (already included but grouped as library)
# Example: datetime, json, collections, re

# 3. Third-party modules:
# External modules installed using pip
# Example: numpy, pandas, matplotlib, requests

# 4. User-defined modules:
# Modules created by programmer in their own project
# Example: mymodule.py, calculator.py

# 5. System/OS modules:
# Used for interacting with system and files
# Example: os, sys, subprocess

#   6. Web modules:
# Used for web development and APIs
# Example: flask, django, requests

# Arguments from command line

import math

print(math.sqrt(16))# it will give the square root of 16
print(math.ceil(3.2))# it will give the upper value of 3.2
print(math.floor(3.8))# it will give the lower value of 3.8
print(math.pi)# it will give the value of pi
print(math.e)# it will give the value of e
print(math.pow(4,2))# it will give the square of 4